# Display a message to the player.
# print() outputs text to the screen.
print("You disembark from a ferry on to Marrowbone Island.")

# Ask the player for their name.
# input() waits for the user to type something and press Enter.
# Whatever they type is stored in the variable named 'name'.
name = input("What is your name, adventurer? ")

# Display a personalized welcome message.
# The f before the quotation marks creates an f-string.
# Curly braces {} insert the value stored in the variable.
print(f"Welcome, {name}! Your quest begins now...")

